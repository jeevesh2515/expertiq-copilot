def seed_experts(db: Session) -> int:
    """
    Seed the database with synthetic expert profiles.

    Checks if experts already exist to avoid duplicates.
    Returns the number of experts inserted.
    """
    existing_count = db.query(Expert).count()
    if existing_count > 0:
        logger.info(f"Database already contains {existing_count} experts. Skipping seed.")
        return 0

    inserted = 0
    for data in EXPERTS:
        expert = Expert(
            name=data["name"],
            title=data["title"],
            company=data["company"],
            industry=data["industry"],
            seniority=data["seniority"],
            bio=data["bio"],
            years_experience=data["years_experience"],
            availability=data["availability"],
            hourly_rate=data.get("hourly_rate"),
        )
        expert.topics = data["topics"]
        expert.publications = data["publications"]
        db.add(expert)
        inserted += 1

    db.commit()
    logger.info(f"Seeded {inserted} expert profiles.")
    return inserted


def run_seed() -> None:
    """Standalone entry point for seeding."""
    init_db()
    db = SessionLocal()
    try:
        count = seed_experts(db)
        print(f"✓ Seeded {count} expert profiles.")
    finally:
        db.close()


if __name__ == "__main__":
    from app.database import init_db
    run_seed()
